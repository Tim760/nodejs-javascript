"use strict";

let text = 'cat';

console.log("text == 'cat'", text == 'cat' ); // checks for equality in value
console.log("text === cat", text === 'cat' ); // Checks for value and strictly matching datatype
console.log("cat < dog", text < 'dog' ); // Checks alphabetical order between strings
console.log("cat > dog", text > 'dog' ); // Checks alphabetical order between strings